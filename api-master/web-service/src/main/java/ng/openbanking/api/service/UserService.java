package ng.openbanking.api.service;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

import java.util.ArrayList;
import java.util.Collection;

@Service("userDetailsService")
public class UserService implements UserDetailsService {


    private final static String TEST_USER = "demo";

    private final static String TEST_PASSWORD = "$2a$10$D4OLKI6yy68crm.3imC9X.P2xqKHs5TloWUcr6z5XdOqnTrAK84ri";


    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        if (!TEST_USER.equalsIgnoreCase(username))
            throw new UsernameNotFoundException("Invalid user " + username);
        return new UserDetails() {
            @Override
            public Collection<? extends GrantedAuthority> getAuthorities() {
                return new ArrayList<>();
            }

            @Override
            public String getPassword() {
                return TEST_PASSWORD;
            }

            @Override
            public String getUsername() {
                return TEST_USER;
            }

            @Override
            public boolean isAccountNonExpired() {
                return true;
            }

            @Override
            public boolean isAccountNonLocked() {
                return true;
            }

            @Override
            public boolean isCredentialsNonExpired() {
                return true;
            }

            @Override
            public boolean isEnabled() {
                return true;
            }
        };
    }
}